            <div class="spacer-1"></div>

            <h3><span class="subtext">Cultural Probes</span></h3>
            <div class="narrow">
              <p>
                We created three cultural probe kits to get a better understanding of our client and left these kits with them for a week. To prevent them from feeling like chores, we designed four crafty activites to help us learn about their daily productivity and their experiences with the company.
              </p>
            </div>

            <figure>
              <a href="img/projects/makerlabs/culturalprobe.png" onclick="target='_blank'"><img src="img/projects/makerlabs/culturalprobe.png" alt="Cultural probe kit with 4 activities"></a>
            </figure>

            <div class="narrow">
              <p>
                Our major finding was that the MakerLabs team wants to invest more resources into the growth of the company, however, they spend a large portion of their time responding to emails and providing answers that are already on the website.
              </p>
              <p>
                We narrowed down our design focus and decided <strong>we want to help MakerLabs improve the way in which they communicate their identity and services to potential customers as well as how they handle customer inquiries</strong>.
              </p>
            </div> <!--end narrow-->

            <div class="spacer-1"></div>

            <h3><span class="subtext">Participatory Workshop</span></h3>
            <div class="narrow">
              <p>
                We held a workshop with four members of the MakerLabs team as a way to co-design with our clients. Our goal was to learn about the inquiries they receive from customers, the causes of long e-mail threads, and how the hierarchy of information on their website should be structured.
              </p>
            </div>

            <figure>
              <a href="img/projects/makerlabs/workshop.png" onclick="target='_blank'"><img src="img/projects/makerlabs/workshop.png" alt="Participatory workshop with 3 activities"></a>
            </figure>

            <div class="narrow">
              <p>
                We found that the MakerLabs staff often have to answer questions (both in person and over e-mail) that are already available on their website because customers are unable to find them. Furthermore, their long e-mail threads often revolve around providing quotes for outsourced projects because customers usually do not provide enough detail in their e-mails.
              </p>
            </div> <!--end narrow-->
